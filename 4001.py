# این کد فقط یک پیغام را در صفحه نمایش (کنسول) نشان می‌دهد.
# print() یک دستور پایتون است که برای چاپ کردن متن یا اطلاعات روی صفحه استفاده می‌شود.
# در داخل print(), متنی که می‌خواهید نشان داده شود را بین علامت‌های نقل قول می‌گذارید. در اینجا، پیامی که چاپ می‌شود این است: "به برنامه پایتون خوش آمدید"

# کاربرد:
# این کد معمولاً در برنامه‌هایی استفاده می‌شود که می‌خواهند به کاربر خوش‌آمد بگویند یا پیامی را در ابتدای برنامه نمایش دهند.

# مفهوم کلی:
# این کد یکی از ساده‌ترین کدهای پایتون است که برای یادگیری نحوه چاپ پیام‌ها و تعامل با کاربر استفاده می‌شود.

print("به برنامه پایتون خوش آمدید")
