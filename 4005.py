# رشته‌ها (Strings) در پایتون

# رشته‌ها یا str، مجموعه‌ای از کاراکترها هستند که برای ذخیره متنی مانند اسم‌ها، جملات 
# و سایر اطلاعات متنی استفاده می‌شوند.
# رشته‌ها در پایتون می‌توانند با استفاده از علامت‌های نقل قول تک (') یا دوتایی (") تعریف شوند.

# ۲.۲. رشته‌ها (Strings)
# در پایتون، رشته‌ها می‌توانند به صورت زیر تعریف شوند:

# رشته با استفاده از نقل قول دوتایی:
name = "علی"   
print("رشته با نقل قول دوتایی:", name)

# رشته با استفاده از نقل قول تک:
greeting = 'سلام'
print("رشته با نقل قول تک:", greeting)

# همچنین، می‌توان از رشته‌ها برای نمایش جملات استفاده کرد:
sentence = "این یک جمله است."
print("جمله:", sentence)

# برای ترکیب دو رشته، می‌توان از عملگر '+' استفاده کرد:
full_greeting = greeting + ", " + name
print("ترکیب رشته‌ها:", full_greeting)

# همچنین می‌توان از فضاهای خالی (Space) برای جداسازی کلمات استفاده کرد:
sentence_with_space = greeting + " " + name
print("جمله با فضای خالی:", sentence_with_space)
