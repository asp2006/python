# هدف: معرفی انواع داده‌های عددی در پایتون (اعداد صحیح و اعشاری) و انجام عملیات ریاضی بر روی آن‌ها

# انواع داده در پایتون

# اعداد صحیح (Integers):
# برای نمایندگی اعداد بدون اعشار از نوع int استفاده می‌شود.
x = 10       # عدد صحیح
print("عدد صحیح:", x)

# اعداد اعشاری (Floating Point Numbers):
# برای نمایندگی اعداد با اعشار از نوع float استفاده می‌شود.
y = 3.14     # عدد اعشاری
print("عدد اعشاری:", y)

# به عنوان مثال دیگر می‌توان به عملگرهای ریاضی بر روی این دو نوع عدد اشاره کرد:
sum_numbers = x + y   # جمع عدد صحیح و اعشاری
product_numbers = x * y  # ضرب عدد صحیح و اعشاری

print("جمع عدد صحیح و اعشاری:", sum_numbers)
print("ضرب عدد صحیح و اعشاری:", product_numbers)
