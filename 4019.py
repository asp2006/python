# مثال 1: گرفتن ورودی از کاربر و تبدیل آن به عدد صحیح
try:
    num = int(input("لطفاً یک عدد صحیح وارد کنید: "))  # دریافت ورودی و تبدیل به عدد صحیح
    print(f"عدد وارد شده: {num}")  # چاپ عدد وارد شده
except ValueError:
    print("لطفاً یک عدد صحیح وارد کنید!")  # پیام در صورت وارد کردن مقدار غیر عددی

# مثال 2: گرفتن ورودی عدد اعشاری از کاربر و تبدیل آن به float
try:
    decimal_num = float(input("لطفاً یک عدد اعشاری وارد کنید: "))  # دریافت ورودی و تبدیل به عدد اعشاری
    print(f"عدد اعشاری وارد شده: {decimal_num}")  # چاپ عدد اعشاری وارد شده
except ValueError:
    print("لطفاً یک عدد اعشاری وارد کنید!")  # پیام در صورت وارد کردن مقدار غیر عددی

# مثال 3: دریافت نام و سن از کاربر در یک خط و تبدیل سن به عدد صحیح
try:
    name, age = input("لطفاً نام و سن خود را وارد کنید (جداسازی با فاصله): ").split()  # دریافت نام و سن از کاربر
    age = int(age)  # تبدیل سن به عدد صحیح
    print(f"نام: {name}, سن: {age}")  # چاپ نام و سن
except ValueError:
    print("لطفاً نام و سن را به درستی وارد کنید. اطمینان حاصل کنید که سن عدد صحیح باشد.")

# نکته: در این مثال اگر کاربر ورودی نادرستی بدهد، برنامه از دستورات try-except برای مدیریت خطا استفاده می‌کند.
