# انواع داده در پایتون
# پایتون از چندین نوع داده مختلف پشتیبانی می‌کند. این انواع داده به دسته‌های مختلفی تقسیم می‌شوند.

# ۲.۱. اعداد (Numbers)
# در پایتون، دو نوع عدد اصلی وجود دارد:

# اعداد صحیح (Integers): برای نمایندگی اعداد بدون اعشار از نوع int استفاده می‌شود.
x = 10       # عدد صحیح
print("عدد صحیح x =", x)

# اعداد اعشاری (Floating Point Numbers): برای نمایندگی اعداد با اعشار از نوع float استفاده می‌شود.
y = 3.14     # عدد اعشاری
print("عدد اعشاری y =", y)

# همچنین می‌توان از عملگرهای ریاضی مانند جمع، تفریق، ضرب و تقسیم استفاده کرد:
sum_result = x + y
print("جمع x و y =", sum_result)

difference_result = x - y
print("تفریق x و y =", difference_result)

multiplication_result = x * y
print("ضرب x و y =", multiplication_result)

division_result = x / y
print("تقسیم x و y =", division_result)
